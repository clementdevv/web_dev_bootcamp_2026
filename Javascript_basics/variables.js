let score = 0 

score = score + 5
console.log(score) // 5

score = score + 5
console.log(score) // 10

score += 5
console.log(score) // 15

score += 5
console.log(score) // 20

score -= 5
console.log(score) // 15

score++ // Same as score = score + 1
++score
console.log(score) // 17

score-- // Same as score = score - 1
--score
console.log(score) // 15